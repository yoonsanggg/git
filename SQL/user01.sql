-- 도구- 환경설정 - 데이터베이스 -NLS
 -- FLOOR : 소수점 버림처리 
 --날짜와 날짜의 연산, 날짜와 숫자의 연산 모두 가능함 (일 수로 나옴)
SELECT SYSDATE, HIRE_DATE,  SYSDATE -   2,  FLOOR(SYSDATE - HIRE_DATE)
