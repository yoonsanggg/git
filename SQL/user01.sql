-- NULL 의 연산결과는 NULL ------> NVL (컬럼, 변경 할 값) 함수를 이용해서 NULL 이면 0으로 치환
--연산식이나 함수를 이용할 경우, 컬럼명이 연산식으로 변경됨
SELECT  EMP_NAME,  NVL(BONUS,0) 보너스율 ,NVL(BONUS,0)* SALARY 보너스,   SALARY * NVL(BONUS,0) +SALARY "보너스 포함한 급여"
FROM    EMP;

